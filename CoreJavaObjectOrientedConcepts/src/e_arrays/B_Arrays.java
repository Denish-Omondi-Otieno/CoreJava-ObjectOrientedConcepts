package e_arrays;

public class B_Arrays {

	public static void main(String[] args) {
		
		// Creating a reference of intVariable
		// In the reference we can initialise variables
		int[] intVariableArray; 
		
		// intVariable Array will contain 10 int variables
		intVariableArray = new int[10];
		
		// Storing a value in an index number of the array
		intVariableArray[5]= 10;
		System.out.println(intVariableArray[5]);
	}

}
