package e_arrays;

public class C_Arrays {

	public static void main(String[] args) {
		
		// We don't need to give the length of the array
		// Java will assign it from the number of elements
		int[] intVariableArray = {1,2,3,4,5,6,7,8,9}; 
		
		System.out.println(intVariableArray[5]);
		
		// A float array
		float[] floatVariableArray = {1.2f, 3.4f, 4.6f, 6.7f};
		// The index value is always an int entity
		System.out.println(floatVariableArray[3]);
	}

}
